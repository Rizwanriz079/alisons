                                          strokeWidth: 2,
                                          valueColor:
                                              AlwaysStoppedAnimation<Color>(
                                            Colors.white,
                                          ),
                                        ),
                                      )
                                    : Text(
                                        'Login',
                                        style: AppTextStyles.heading3.copyWith(
                                          color: Colors.white,
